# ============================================================
# 인덱싱과 슬라이싱
# 문자열을 만들면 각 문자에 인덱스 번호가 자동으로 부여된다.
# 인덱스 번호는 앞에서부터는 0, 뒤에서부터는 -1로 시작한다.
# ============================================================

a = "Hello" #  H   e   l   l   o
            #  0   1   2   3   4
            # -5  -4  -3  -2  -1

# 인덱싱 : 특정 문자만 가져옴
print(a[0]) # H
print(a[1]) # e
print(a[-3]) # l
print(a[-2]) # l
print(a[-1]) # o

# 슬라이싱 : 특정 구간을 가져옴
print(a[2:4]) # ll -> 인덱스번호가 2-3인 구간을 가져옴. 
print(a[:3]) # Hel -> 처음부터 인덱스 번호가 2인 구간을 가져옴(처음부터 시작일 때는 콜론 앞 숫자를 생략 가능)
print(a[2:]) # llo -> 인덱스 번호가 2인 문자부터 끝까지 가져옴(끝이 마지막 문자라면 콜론 뒤 숫자를 생략 가능)




# ============================================================
# 문자열 포매팅 : 문자열에 변수를 대입하고 싶을 때 사용하는 방법
# 문자열 포맷 코드
# %s : 문자열
# %c : 문자 한 개
# %d : 정수형
# %f : 실수형
# %o : 8진수형
# %x : 16진수형
# %% : %자체
# ============================================================

# 숫자 바로 대입
print("현재 온도는 %d도입니다."%18) # 현재 온도는 18도입니다.

# 문자열 바로 대입
print("현재 날씨는 %s입니다."%"흐림") # 현재 날씨는 흐림입니다.

# 2개 이상의 값을 넣기
print("현재 온도는 %d도이고 날씨는 %s입니다."%(3, "흐림")) # 현재 온도는 3도이고 날씨는 흐림입니다.

# 포맷 코드와 숫자 함께 사용
print("%10s"%"hi") #         hi(전체 공간을 10개로 설정하고 문자열을 오른쪽 정렬)
print("%-10shello"%"hi") # hi        hello(전체 공간을 10개로 설정한 후 hi를 왼쪽 정렬)
print("%0.4f"%0.123456) # 0.1235 -> 소수점을 n번째 자리에서 반올림




# ============================================================
# format함수를 통한 포맷팅
# ============================================================

# 숫자와 문자열 대입하기
print("오늘은 {0}도입니다.".format(3)) # 오늘은 3도입니다.
print("오늘의 날씨는 {0}입니다.".format("흐림")) # 오늘의 날씨는 흐림입니다.

# 여러 값 한 번에 대입하기
print("오늘은 {0}도이고 날씨는 {1}입니다.".format(3, "흐림")) # 오늘은 3도이고 날씨는 흐림입니다.

# 이름을 사용하여 대입하기
print("오늘은 {temp}도이고 날씨는 {weather}입니다.".format(temp = 3, weather = "흐림")) # 오늘은 3도이고 날씨는 흐림입니다.

# 정렬
# 왼쪽 정렬(<)
# 오른쪽 정렬(>)
# 가운데 정렬(^)
print("오늘은 {0:<10}도이고 날씨는 {1:>10}이며 계절은 {2:^10}입니다.".format(3, "흐림", "겨울"))
# 오늘은 3         도이고 날씨는         흐림이며 계절은     겨울    입니다.

# 공백 채우기
print("{0:=^10}".format("hi")) # ====hi====

# 소수점 표현하기
print("{0:.4f}".format(0.12345)) # 0.1235

# 문자 표현하기
# format함수를 사용하여 포맷팅을 할 시 중괄호를 표현하고 싶다면 중괄호를 두 번 써주면 된다.
print("{{and}}".format()) # {and}




# ============================================================
# f-string : 문자열 앞에 f를 붙여 사용할 수 있다.
# ============================================================

temp = 3
weather = "흐림"

print(f"오늘은 {temp}도이고 날씨는 {weather}입니다.") # 오늘은 3도이고 날씨는 흐림입니다.

# f-string에서도 format 함수처럼 정렬, 소수점 표현이 가능하다.
print(f"{'hi':=^10}") # ====hi====
print(f"{0.12345:.4f}") # 0.1235



# ============================================================
# 문자열 관련 함수
# ============================================================
sen = " Hello I love python "

# count() : 문자열에서 특정 문자의 개수를 반환한다.
print(sen.count("l")) # 3

# find() : 문자열에서 특정 문자가 처음 등장하는 인덱스를 반환한다. 없으면 -1을 반환한다.
print(sen.find("H")) # 1

# index() : find()함수와 비슷하지만 없으면 에러가 발생한다.
print(sen.index("I")) # 7

# join() : 문자열의 문자 사이에 특정 문자를 삽입한다.
print(",".join(sen)) # ,H,e,l,l,o, ,I, ,l,o,v,e, ,p,y,t,h,o,n, 

# upper() : 문자열의 문자를 대문자로 바꾼다.
print(sen.upper()) # HELLO I LOVE PYTHON 

# lower() : 문자열의 문자를 소문자로 바꾼다.
print(sen.lower()) # hello i love python 

# lstrip() : 문자열의 왼쪽 공백을 없앤다.
print(sen.lstrip()) # Hello I love python(공백 한칸 존재)

# rstrip() : 문자열의 오른쪽 공백을 없앤다.
print(sen.rstrip()) #  Hello I love python

# strip() : 문자열의 공백을 없앤다.
print(sen.strip()) # Hello I love python

# replace() : 문자열에서 특정 문자를 다른 문자로 치환한다.
print(sen.replace("l", "o")) # Heooo I oove python

# split() : 문자열을 특정 기준으로 나눠준다.(스페이스, 탭 등)
print(sen.split()) # ['Hello', 'I', 'love', 'python']

# isalpha() : 문자열이 알파벳으로만 구성되어있는지 알려준다.
print(sen.isalpha()) # False

# isdigit() : 문자열이 숫자로만 구성되어있는지 알려준다.
print(sen.isdigit()) # False

# startswith() : 문자열이 특정 문자로 시작하는 지 알려준다.
print(sen.startswith(" Hello")) # True

# endswith() : 문자열이 특정 문자로 끝나는 지 알려준다.
print(sen.endswith("love")) # False