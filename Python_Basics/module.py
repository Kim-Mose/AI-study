# 모듈 불러오기
import math

# math 모듈의 함수 사용
print(math.sqrt(16)) # 4.0 -> 제곱근
print(math.pow(2, 3)) # 8.0 -> 거듭제곱
print(math.pi) # 3.141592653589793 -> 원주율

# 특정 함수만 불러오기
from math import sqrt, pi

print(sqrt(16)) # 4.0 -> math.을 붙이지 않아도 된다.
print(pi) # 3.141592653589793

# 별칭(alias) 사용하기
import math as m

print(m.sqrt(16)) # 4.0

# 모듈의 모든 함수 불러오기 -> 권장하지 않음(이름 충돌 가능성)
from math import *

print(sqrt(16)) # 4.0


