# random : 랜덤 관련
import random

print(random.randint(1, 10)) # 1~10 사이의 랜덤 정수
print(random.random()) # 0~1 사이의 랜덤 실수
print(random.choice([1, 2, 3, 4, 5])) # 리스트에서 랜덤으로 하나 선택

# os : 운영체제 관련
import os

print(os.getcwd()) # 현재 작업 디렉토리 경로
print(os.listdir()) # 현재 디렉토리의 파일 목록

# datetime : 날짜와 시간 관련
import datetime

now = datetime.datetime.now()
print(now) # 현재 날짜와 시간
print(now.year) # 현재 연도
print(now.month) # 현재 월
print(now.day) # 현재 일
