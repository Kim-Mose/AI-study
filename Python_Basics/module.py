# ============================================================
# if __name__ == "__main__"
# 해당 파일이 직접 실행될 때만 아래 코드를 실행한다.
# 다른 파일에서 import할 때는 실행되지 않는다.
# ============================================================

def add(a, b):
    return a + b

if __name__ == "__main__":
    print(add(1, 2)) # 3 -> 이 파일을 직접 실행할 때만 출력
    # 다른 파일에서 import module 하면 이 부분은 실행되지 않는다.


