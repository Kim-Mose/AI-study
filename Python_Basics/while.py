# 짝수!
# 홀수!
# 짝수!
# 홀수!
# 짝수!
# 끝!


# 중첩 while문으로 구구단 만들기

dan = 1
num = 1

while dan <= 9:
    while num <= 9:
        print(f"{dan} * {num} = {dan * num}")
        num += 1
    dan += 1
    num = 1

# 1 * 1 = 1
# 1 * 2 = 2
# 1 * 3 = 3
# ...
# 1 * 9 = 9
# 2 * 1 = 2
# 2 * 2 = 4
# ...
# 9 * 9 = 81