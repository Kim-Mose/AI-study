# ============================================================
# continue와 break : while문에서 쓸 수 있는 기능이다.
# continue : while문 코드블럭의 처음으로 돌아간다.
# break : while문을 종료한다.
# ============================================================

# 일부러 무한루프에 걸린 후 break를 통해 탈출하는 예제이다.

num = 0

while True:
    if num == 10:
        break # num이 10이 되면 break가 발생해 while문을 탈출한다.
    num += 1
print("break문 사용")

# continue로 홀짝 판별기 만들기

num = 0

while num <= 10:
    if num % 2 == 0:
        print("짝수!")
        num += 1
        continue # 원래라면 밑에 홀수! 까지 출력되어야하지만 continue로 인해 실행되지 않고 처음으로 돌아간다.
    print("홀수!")
    num += 1

