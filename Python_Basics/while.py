# ============================================================
# while - else문과 중첩 while문
# while - else : while문이 정상적으로 종료되었을 때 else문을 실행한다. -> break로 빠져나오면 실행되지 않는다.
# 중첩 while : while문 안에 while문을 넣는 것이다.
# ============================================================

# while - else문으로 홀짝 판별기 만들기

num = 0

while num <= 10:
    if num % 2 == 0:
        print("짝수!")
    else:
        print("홀수!")
    num += 1
else:
    print("끝!")

