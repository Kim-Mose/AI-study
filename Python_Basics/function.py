# 기본적인 함수의 모양

def add(a, b):  # 함수를 정의할 때는 def를 사용하며 전달된 값을 받는 변수인 매개변수를 써준다. -> 콜론과 들여쓰기 주의!
    sum = a + b # 함수의 기능
    return sum  # 반환값

# 함수 호출 방법

sum = add(1, 2) # 함수 호출 시 함수의 이름과 함수에 전달할 값인 인자를 써준다.
print(sum) # 3

