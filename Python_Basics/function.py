# *args 사용

def add(*args): # 들어온 값들을 튜플로 처리
    res = 0
    for i in args:
        res += i
    return res

res1 = add(1, 2)
res2 = add(1, 2, 3)

print(res1, res2) # 3 6

# **kwargs 사용

def info(**kwargs):
    for key, val in kwargs.items():
        print(key, val)

info(name="홍길동", age = 20, gender = "M", address = "서울특별시") # name 홍길동 / age 20 / gender M / address 서울특별시

# *args, **kwargs 동시에 사용

def mixed_function(name, *args, **kwargs):
     print(f"이름: {name}")
     print(f"추가 인수들: {args}")
     print(f"키워드 인수들: {kwargs}")
 
mixed_function('홍길동', 1, 2, 3, age=20, city='address') # 이름: 홍길동 / 추가 인수들: (1, 2, 3) / 키워드 인수들: {'age': 20, 'city': 'address'}

